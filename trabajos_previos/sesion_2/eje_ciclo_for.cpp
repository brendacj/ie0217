# include <iostream>
using namespace std;
int main () {
    // Se ejecuta el bloque dentro del ciclo desde i = 1 hasta
    // i = 5, aumentando i en 1.
    // También se podría recorrer un elemento iterable, como una lista, directamente
    for (int i =1; i <= 5; ++i) {
        cout << i << " ";
    }
    return 0;
}